public class ShortOperationAppl {
    public static void main(String[] args) {
        int a = 10;
        a += 5; // a = a + 5;
        System.out.println(a);
        a /= 3; // a = a / 3;
        System.out.println(a);
        a *= 10; // a = a * 10;
        System.out.println(a);
        a -= 20; // a = a - 20;
        System.out.println(a);
        a++; // a = a + 1;
        System.out.println(a);
        ++a; // a = a + 1;
        System.out.println(a);
        a--; // a = a - 1;
        System.out.println(a);
    }
}
