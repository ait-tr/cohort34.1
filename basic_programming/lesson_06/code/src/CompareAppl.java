public class CompareAppl {
    public static void main(String[] args) {
        int x = 10;
        int y = 15;
        boolean check = x > y;
        System.out.println(check);
        check = x < y;
        System.out.println(check);
        check = x == 10;
        System.out.println(check);
        check = y != 15;
        System.out.println(check);
        check = y >= 15;
        System.out.println(check);
        check = x <= 15;
        System.out.println(check);
    }
}
